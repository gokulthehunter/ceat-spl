        jQuery(document).ready(function($){
            if($(window).width() > 767){
                $(".about_info_btns").stick_in_parent({
                    parent: ".tab_wraps",
                    offset_top:150
                });
            };

            // function tab actions[start]
            function verticalTabActs(sel){
                // if(desktop){
                    $(document).on("click",sel,function(){
                        var _this = $(this),
                            _sec = _this.attr("data-sec"),
                            _off = parseInt(_this.attr("data-offset"));

                        $(sel).removeClass("state-active");
                        $(this).addClass("state-active");
                        $("body,html").animate({
                            scrollTop:$("#"+_sec).offset().top - _off
                        },1000);
                    });
                    $(window).on("scroll load",function(){
                        var _posW = $(window).scrollTop();
                            // _posS = 
                            $(".award-set").each(function (i) {
                                if (($(this).offset().top - 100) <= _posW) {
                                    $(".about_info_btns--each.state-active").removeClass("state-active");
                                    $(".about_info_btns--each").eq(i).addClass("state-active");
                                }
                            })
                    })
                // }
            };verticalTabActs(".about_info_btns--each")
            // function tab actions[end]
        }(jQuery))
